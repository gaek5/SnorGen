//######################################################################
void FPB::print()
{
	printf("%10llu	%10llu	%10llu\n",flow, pkt, byte);

}
//######################################################################
void FPB::print(FILE *p_fp)
{
	fprintf(p_fp, "%10llu	%10llu	%10llu\r\n",flow, pkt, byte);
}

//######################################################################
void FPB::setFlow(u_int64_t p_uiFlow)
{
	this->flow = p_uiFlow;
}
//######################################################################
void FPB::setPkt(u_int64_t p_uiPkt)
{
	this->pkt = p_uiPkt;
}
//######################################################################
void FPB::setByte(u_int64_t p_uiByte)
{
	this->byte = p_uiByte;
}



//######################################################################
u_int64_t FPB::getFlow()
{
	return this->flow;
}
//######################################################################
u_int64_t FPB::getPkt()
{
	return this->pkt;
}
//######################################################################
u_int64_t FPB::getByte()
{
	return this->byte;
}
